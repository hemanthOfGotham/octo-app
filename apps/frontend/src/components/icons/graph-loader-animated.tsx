type GraphLoaderAnimatedProps = {
	className?: string;
	style?: CSSProperties;
	loop?: boolean;
	title?: string;
};

function usePrefersReducedMotion(): boolean {
	const [reduced, setReduced] = useState(false);
	useEffect(() => {
		if (typeof window === 'undefined' || !window.matchMedia) {
			return;
		}
		const mql = window.matchMedia('(prefers-reduced-motion: reduce)');
		setReduced(mql.matches);
		const onChange = () => setReduced(mql.matches);
		mql.addEventListener('change', onChange);
		return () => mql.removeEventListener('change', onChange);
	}, []);
	return reduced;
}

export default function GraphLoaderAnimated({
	className,
	style,
	loop = true,
	title = 'Loading chart',
}: GraphLoaderAnimatedProps) {
	const reduced = usePrefersReducedMotion();
	const repeatCount = loop ? 'indefinite' : '1';
	const fill = loop ? undefined : 'freeze';

	return (
		<div className={cn('graph-loader', className)} style={style} role='img' aria-label={title}>
			<svg viewBox={VIEW_BOX} fill='none' xmlns='http://www.w3.org/2000/svg' preserveAspectRatio='xMidYMid meet'>
				<title>{title}</title>
				{LAYERS.map((layer, idx) => {
					let node = (
						<path
							d={layer.d.base}
							fill='none'
							strokeLinecap={layer.linecap}
							strokeLinejoin={layer.linejoin}
						>
							{!reduced && layer.d.track && (
								<animate
									attributeName='d'
									dur={DURATION}
									repeatCount={repeatCount}
									fill={fill}
									calcMode='spline'
									values={layer.d.track.values}
									keyTimes={layer.d.track.keyTimes}
									keySplines={layer.d.track.keySplines}
								/>
							)}
						</path>
					);

					if (layer.anchor) {
						node = <g transform={`translate(${layer.anchor})`}>{node}</g>;
					}

					node = (
						<g transform={`scale(${layer.scale.base})`}>
							{!reduced && layer.scale.track && (
								<animateTransform
									attributeName='transform'
									type='scale'
									dur={DURATION}
									repeatCount={repeatCount}
									fill={fill}
									calcMode='spline'
									values={layer.scale.track.values}
									keyTimes={layer.scale.track.keyTimes}
									keySplines={layer.scale.track.keySplines}
								/>
							)}
							{node}
						</g>
					);

					if (layer.rotate) {
						node = <g transform={`rotate(${layer.rotate})`}>{node}</g>;
					}

					node = (
						<g transform={`translate(${layer.position.base})`}>
							{!reduced && layer.position.track && (
								<animateTransform
									attributeName='transform'
									type='translate'
									dur={DURATION}
									repeatCount={repeatCount}
									fill={fill}
									calcMode='spline'
									values={layer.position.track.values}
									keyTimes={layer.position.track.keyTimes}
									keySplines={layer.position.track.keySplines}
								/>
							)}
							{node}
						</g>
					);

					return <g key={idx}>{node}</g>;
				})}
			</svg>
		</div>
	);
}
